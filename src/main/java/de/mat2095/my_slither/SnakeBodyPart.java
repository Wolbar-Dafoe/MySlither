package de.mat2095.my_slither;


class SnakeBodyPart {

    double x, y;
    /**
     * Following code is used to save inputs to local variables of the same name.
     * @param x - x coordinate
     * @param y - y coordinate
     */
    SnakeBodyPart(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
